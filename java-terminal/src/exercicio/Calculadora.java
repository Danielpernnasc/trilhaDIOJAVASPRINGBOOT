public class Calculadora {
    /**
     * Este método realiza a soma de dois números inteiros.
     * 
     * @param numeroUm   este é o primeiro paramentro do método
     * @param numeroDois este é o segundo paramentro do método
     * @return int o resultado deste método é a soma dos dois numeros.
     */
    public int soma(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }

}
